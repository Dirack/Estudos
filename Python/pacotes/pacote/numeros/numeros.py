def somar(n1, n2):
	"""
	Somar dois números
	"""
	return n1+n2

def subtrair(n1, n2):
	"""
	Subtrair dois números
	"""
	return n1-n2

def multiplicar(n1, n2):
	"""
	Multiplicar dois números
	"""
	return n1*n2

def dividir(n1, n2):
	"""
	Dividir dois números
	"""
	return n1/n2
